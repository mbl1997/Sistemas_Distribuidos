public class Atualizacao {
    private int sensor, valor;

    public Atualizacao(int sensor, int valor) {
        this.sensor = sensor;
        this.valor = valor;
    }

    public int getSensor() {
        return sensor;
    }

    public int getValor() {
        return valor;
    }
    
    
}
